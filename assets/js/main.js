/* Expose to window so inline scripts on other pages can access it */
window.maherTranslations = maherTranslations;

function maherApplyLanguage(lang) {
    var root = document.documentElement;
    var currentLang = lang === "en" ? "en" : "ar";
    root.lang = currentLang;
    /* Keep dir="rtl" always — the site layout is RTL-only.
       Changing dir breaks flex/grid directions on all pages. */
    localStorage.setItem("maherLang", currentLang);

    var dict = maherTranslations[currentLang] || {};

    document.querySelectorAll("[data-i18n]").forEach(function (el) {
        var key = el.getAttribute("data-i18n");
        if (dict[key]) {
            el.innerHTML = dict[key];
        }
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach(function (el) {
        var key = el.getAttribute("data-i18n-placeholder");
        if (dict[key]) {
            el.setAttribute("placeholder", dict[key]);
        }
    });

    document.querySelectorAll("[data-i18n-value]").forEach(function (el) {
        var key = el.getAttribute("data-i18n-value");
        if (dict[key]) {
            el.value = dict[key];
        }
    });

    document.querySelectorAll(".lang-btn").forEach(function (btn) {
        if (btn.getAttribute("data-lang") === currentLang) {
            btn.classList.add("lang-btn-active");
        } else {
            btn.classList.remove("lang-btn-active");
        }
        var key = btn.getAttribute("data-i18n");
        if (key && dict[key]) {
            btn.textContent = dict[key];
        }
    });

    var yearSpan = document.getElementById("year");
    if (yearSpan) {
        yearSpan.textContent = new Date().getFullYear().toString();
    }

    /* Notify other scripts that language changed (e.g. to re-render dynamic grids) */
    document.dispatchEvent(new CustomEvent("maherLangChanged", { detail: { lang: currentLang } }));

    /* Remove loading class to reveal page — prevents flash of untranslated content */
    document.documentElement.classList.remove("maher-loading");
}

document.addEventListener("DOMContentLoaded", function () {
    var navToggle = document.getElementById("navToggle");
    var mainNav = document.getElementById("mainNav");

    if (navToggle && mainNav) {
        // Calculate real header height and set nav top dynamically
        function setNavTop() {
            var header = document.querySelector(".main-header");
            if (header) {
                mainNav.style.top = header.offsetHeight + "px";
            }
        }
        setNavTop();
        window.addEventListener("resize", setNavTop);

        navToggle.addEventListener("click", function () {
            mainNav.classList.toggle("open");
            setNavTop(); // recalc in case header height changed
        });

        // Close nav when a link inside it is clicked (mobile)
        mainNav.querySelectorAll("a").forEach(function (link) {
            link.addEventListener("click", function () {
                mainNav.classList.remove("open");
            });
        });

        // Close nav when clicking outside
        document.addEventListener("click", function (e) {
            if (!mainNav.contains(e.target) && !navToggle.contains(e.target)) {
                mainNav.classList.remove("open");
            }
        });
    }

    var langFromStorage = localStorage.getItem("maherLang") || "ar";

    maherApplyLanguage(langFromStorage);

    document.querySelectorAll(".lang-btn").forEach(function (btn) {
        btn.addEventListener("click", function () {
            var lang = btn.getAttribute("data-lang") || "ar";
            maherApplyLanguage(lang);
        });
    });

    var currentPath = window.location.pathname.split("/").pop() || "index.html";
    document.querySelectorAll(".main-nav .nav-link").forEach(function (link) {
        var href = link.getAttribute("href");
        if (href === currentPath) {
            link.classList.add("active");
        }
    });

    var seekerTypeBtn = document.getElementById("registerTypeSeeker");
    var employerTypeBtn = document.getElementById("registerTypeEmployer");
    var seekerForm = document.getElementById("registerSeekerForm");
    var employerForm = document.getElementById("registerEmployerForm");

    function setRegisterType(type) {
        if (!seekerForm || !employerForm || !seekerTypeBtn || !employerTypeBtn) return;
        if (type === "employer") {
            seekerForm.style.display = "none";
            employerForm.style.display = "block";
            seekerTypeBtn.classList.remove("active");
            employerTypeBtn.classList.add("active");
        } else {
            seekerForm.style.display = "block";
            employerForm.style.display = "none";
            seekerTypeBtn.classList.add("active");
            employerTypeBtn.classList.remove("active");
        }
    }

    if (seekerTypeBtn && employerTypeBtn) {
        seekerTypeBtn.addEventListener("click", function () {
            setRegisterType("seeker");
        });
        employerTypeBtn.addEventListener("click", function () {
            setRegisterType("employer");
        });
    }
});
